//Operadores logicos

let a =true;
let b = false;

//El operador logico && (And o Y)
//Regresa verdadero solo si ambos valores son verdaderos



console.log(`${a} && ${b}-> ${a&&b}`);


//El operador logico || (Or o O)
//Regresa verdadero si cualquiera de los valores es verdadero

console.log(`${a} || ${b} -> ${a||b}`)


// Operador ! (not o NO)
// Invierte el valor original, Si es true pasa a falso y visceversa

console.log(`${a}->${!a}`);