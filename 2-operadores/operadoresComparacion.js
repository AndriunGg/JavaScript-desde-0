 //Operadores Relacionales (Comparacion)
 let a = 5;
 let b = '5';

 console.log(a);
 console.log(b);

//Operador igualdad ==
//Compara solo valores y hace una conversion si es necesario
console.log("a==b ->", a==b);

//operador igualdad estricta o exacta

console.log("a===b ->", a===b);

//String interpolation
console.log(`este es el valor de a ${a}===${b} -> ${a===b}` );


//Operador distintos
//Compara valor y convierte el tipo de dato si es necesario
console.log(`${a}!=${b} -> ${a != b}` );

//comparador distinto exacto
//Compara el valor y el tipo de dato
console.log(`${a}!==${b} -> ${a !== b}` );


//operador menor que
console.log(`${a}<${b} -> ${a<b}` );

//operador menor o igual que 
console.log(`${a}<=${b} -> ${a<=b}` );

//operador mayor que

console.log(`${a}>${b} -> ${a>b}` );

//operador mayor o igual

console.log(`${a}>=${b} -> ${a>=b}` );




