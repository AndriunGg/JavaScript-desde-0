//operadores de asigacion

let miNumero = 10;

console.log(miNumero);

miNumero= 20;
console.log(miNumero);

//operador +=

miNumero += 5;// al valor de la variable le sumamos 5

console.log(miNumero);
//Operador -=


miNumero -=3;//al valor de la variable se le resta 3
console.log(miNumero);
//operador *=

miNumero *=2;// Al valor de la variable se le multiplica por 2
console.log(miNumero);
//operador /=

miNumero /= 4; // Al valor de la variable se le divide por 4
console.log(miNumero);
//operador %=

miNumero %=2;
console.log(miNumero);

